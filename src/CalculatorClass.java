public class CalculatorClass {
    int result;
    void add(int a,int b){
        result=a+b;
        System.out.println("Sum = "+result);
    }
    void sub(int a,int b){
        result=a-b;
        System.out.println("Sum = "+result);
    }
    void mul(int a,int b){
        result=a*b;
        System.out.println("Sum = "+result);
    }
    void div(int a,int b){
        result=a/b;
        System.out.println("Sum = "+result);
    }
}
